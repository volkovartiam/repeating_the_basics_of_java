package ru.volkov.lesson5.stream.model;

public class SeaCreature {
    private String name;
    private int weight;
    private Species species;
    private String type;

    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }

    public Species getSpecies() {
        return species;
    }

    public String getType() {
        return type;
    }
}
